package com.gildedrose.domain.service;


import com.gildedrose.domain.Item;
import com.gildedrose.domain.event.ItemCreatedEvent;
import com.gildedrose.domain.event.ItemUpdatedEvent;
import com.gildedrose.domain.validation.CanValidate;

import java.time.LocalDateTime;

class GildedRoseDomainServiceImpl implements GildedRoseDomainService {
    Item[] items;

    public GildedRoseDomainServiceImpl(Item[] items) {
        this.items = items;
    }

    public void updateQuality() {
        for (int i = 0; i < items.length; i++) {
            if (!items[i].name.equals("Aged Brie")
                && !items[i].name.equals("Backstage passes to a TAFKAL80ETC concert")) {
                if (items[i].quality > 0) {
                    if (!items[i].name.equals("Sulfuras, Hand of Ragnaros")) {
                        items[i].quality = items[i].quality - 1;
                    }
                }
            } else {
                if (items[i].quality < 50) {
                    items[i].quality = items[i].quality + 1;

                    if (items[i].name.equals("Backstage passes to a TAFKAL80ETC concert")) {
                        if (items[i].sellIn < 11) {
                            if (items[i].quality < 50) {
                                items[i].quality = items[i].quality + 1;
                            }
                        }

                        if (items[i].sellIn < 6) {
                            if (items[i].quality < 50) {
                                items[i].quality = items[i].quality + 1;
                            }
                        }
                    }
                }
            }

            if (!items[i].name.equals("Sulfuras, Hand of Ragnaros")) {
                items[i].sellIn = items[i].sellIn - 1;
            }

            if (items[i].sellIn < 0) {
                if (!items[i].name.equals("Aged Brie")) {
                    if (!items[i].name.equals("Backstage passes to a TAFKAL80ETC concert")) {
                        if (items[i].quality > 0) {
                            if (!items[i].name.equals("Sulfuras, Hand of Ragnaros")) {
                                items[i].quality = items[i].quality - 1;
                            }
                        }
                    } else {
                        items[i].quality = items[i].quality - items[i].quality;
                    }
                } else {
                    if (items[i].quality < 50) {
                        items[i].quality = items[i].quality + 1;
                    }
                }
            }
        }
    }

    @Override
    public ItemCreatedEvent createItem(CanValidate<Item> item) {
        return item.validate()
            .map(validatedItem -> new ItemCreatedEvent(validatedItem, LocalDateTime.now()))
            .getValue();
    }

    @Override
    public ItemUpdatedEvent updateItem(CanValidate<Item> item) {
        return item.validate()
            .map(validatedItem -> new ItemUpdatedEvent(validatedItem, LocalDateTime.now()))
            .getValue();
    }
}
